		<h1 class="blog-title">Motion Designer Wanted</h1>
		<h4 class="blog-publisher"><span class="text-l-gray">Posted by</span> John Doe</h4>

		<h5>Junior to Mid-level Motion Designer Wanted.</h5>
		<p class="text-l-gray">Buzzooka is looking for a full-time motion designer to join our Cebu-based team. You will work with our design team, project manager and creative directors to help produce design work for motion and branding projects. This job is 10% research, 30% brainstorming, 60% making high quality style frames.</p>

		<h6>We're looking For:</h6>
		<p class="text-l-gray">– Excellent Photoshop skills (e.g. photo-compositing)<br>
		   – Understanding of story and sequence design<br>
		   – Strong and dynamic compositions<br>
		   – Solid typography</p>

		<h6>Nice to Have:</h6>
		<p class="text-l-gray">
			– Animation and/or 3D skills (AE, C4D, etc.)<br>
		– Some experience in print or web (we also do a lot of branding work if you’re 
		interested)<br>
		– Illustration and drawing skills
		</p>

		<h6>About You:</h6>
		<p class="text-l-gray">You’re someone who likes to think before you create. You come into every 
		project with an open mind, curiosity to explore, and aren’t confined to a style. 
		You work quickly, like to iterate, and don’t fall in love with your first idea. You 
		love brainstorming and refining ideas till they tell a powerful story.
		</p>

		<p class="text-l-gray">
		You appreciate beautiful imagery and exquisite typography. Hence, you spend 
		a lot of time on Pinterest, curating and collecting.</p>

		<p class="text-l-gray">
		You want to work in an environment that’s fun, laid-back, yet highly creative. 
		You want to work with experienced staff and specialized freelancers so you can 
		learn and grow your own skillset.</p>

		<p class="text-l-gray">
		You’re not afraid to say what you think, but you’re not a jerk about it. You 
		ideally would have at least one year of experience working in the motion 
		industry.</p>

		<h6>About Buzzooka</h6>
		<p class="text-l-gray">A small and powerful team from Cebu, Philippines. We create quality products
		that enrich the lives of others. Our story begins from 2012, since then we created 
		more than 200 projects, and got a lot of positive feedback from our happy clients.</p>
		
		<h6>Our Work Environment</h6>
		<p class="text-l-gray">Relaxed, open, and nurturing. The people we bring in align with our core values 
		and goals. It doesn’t matter where you came from, or if you went to school. We’ll 
		take you if you have the right attitude and an excellent portfolio.</p>
		<p class="text-l-gray">
		This is an opportunity for a young designer to work with and learn from a team that 
		has seen the industry in its infancy and through its many stages of evolution.</p>

		<hr class="mt-5 mb-5" />

